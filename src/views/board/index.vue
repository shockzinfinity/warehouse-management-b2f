<template lang="pug">
  v-card board
    v-card-title board test
    v-data-table(
      :headers="headers"
      :items="items"
    )
      template(v-slot:item.id="{ item }")
        v-btn(icon @click="openDialog(item)") <v-icon>mdi-pencil</v-icon>
        v-btn(icon @click="remove(item)") <v-icon>mdi-delete</v-icon>
    v-card-actions
      //- v-btn(@click="read") <v-icon>mdi-page-next-outline</v-icon>
      v-btn(@click="openDialog(null)") <v-icon>mdi-pencil</v-icon>
    v-dialog(max-width="500" v-model="dialog")
      v-card
        v-form
          v-card-text
            v-text-field(v-model="form.title")
            v-text-field(v-model="form.content")
          v-card-actions
            v-spacer
            v-btn(@click="update" v-if="selectedItem") update
            v-btn(@click="add" v-else) save
</template>

<script>
export default {
  data () {
    return {
      headers: [
        { value: 'title', text: '제목' },
        { value: 'content', text: '내용' },
        { value: 'id', text: 'Id' }
      ],
      items: [],
      form: {
        title: '',
        content: ''
      },
      dialog: false,
      selectedItem: null,
      unsubscribe: null
    }
  },
  created () {
    // this.read()
    this.subscribe()
  },
  destroyed () {
    if (this.unsubscribe) {
      this.unsubscribe()
    }
  },
  methods: {
    subscribe () {
      this.unsubscribe = this.$firebase.firestore().collection('boards').onSnapshot(sn => {
        if (sn.empty) {
          this.items = []
          return
        }

        // console.log('here')
        this.items = sn.docs.map(v => {
          const item = v.data()
          return {
            id: v.id,
            title: item.title,
            content: item.content
          }
        })
      })
    },
    openDialog (item) {
      this.selectedItem = item
      this.dialog = true
      if (!item) {
        this.form.title = ''
        this.form.content = ''
      } else {
        this.form.title = item.title
        this.form.content = item.content
      }
    },
    add () {
      this.$firebase.firestore().collection('boards').add(this.form)
      this.dialog = false
    },
    async read () {
      const sn = await this.$firebase.firestore().collection('boards').get()
      sn.docs.forEach(v => {
        console.log(v.id)
        console.log(v.data())
      })

      this.items = sn.docs.map(v => {
        const item = v.data()
        return {
          id: v.id,
          title: item.title,
          content: item.content
        }
      })

      // console.log(this.items)
    },
    update () {
      this.$firebase.firestore().collection('boards').doc(this.selectedItem.id).update(this.form)
      this.dialog = false
    },
    remove (item) {
      this.$firebase.firestore().collection('boards').doc(item.id).delete()
    }
  }
}
</script>
